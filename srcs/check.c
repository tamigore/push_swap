#include "push_swap.h"

int	check_all(t_pile *lst)
{
	int	i;

	i = 0;
	if (lst->max_b != 0)
		return (0);
	while (i < lst->max_a)
	{
		if (lst->a[i] != i)
			return (0);
		i++;
	}
	return (1);
}
