#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*copy;
	int		i;

	if (!src)
		return (NULL);
	i = ft_strlen(src);
	copy = (char *)malloc(i + 1);
	if (!copy)
		return (NULL);
	i = 0;
	while (src[i])
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
